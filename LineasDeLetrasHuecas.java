public class LineasDeLetrasHuecas {
    public static void main(String[] args) {
        int i = 0;
        while (i < 4) {
            int n = 0;
            while (n < 5 ) {
                System.out.print("A");
                n++;
            }
            i++;
            System.out.println();
        }
    }
}
