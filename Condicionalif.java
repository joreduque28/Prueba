public class Condicionalif {
    public static void main(String[] args) {
        int age = 15;
        if(age>= 18) {
            System.out.println("Eres Mayor de edad, puedes solicitar tu licencia para Conducir.");
            System.out.println("Tramita todo lo necesario en la secretaria de Transito de tú departamento.");
        }
        else {
            System.out.println("Aún eres menor de edad, no puedes tramitar tu licencia para conducir.");
            System.out.println("Solamente si un mayor de edad autoriza puedes obetener este documento.");
        }
    }
}
