public class ComillasVisibles {
    public static void main(String[] args) {
        System.out.println("Es importante tener presente lo que se encuentra dentro de \"Comillas\"");
        char c = '\"';
        System.out.println(c);
        char d = '"';
        System.out.println(d);

        System.out.println("\tDios nos ama a todos, \tPor eso envió a su Hijo Jesús");

        System.out.println("0\t1\t2\t3");
        System.out.println("0\t10\t20\t30");
        System.out.println("0\t100\t200\t300");
    }
}
